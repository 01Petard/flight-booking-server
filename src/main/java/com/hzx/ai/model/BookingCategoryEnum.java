package com.hzx.ai.model;

/**
 * 航班舱位等级枚举
 * 
 * <p>定义航班预订系统中可选择的舱位等级，每个等级对应不同的服务标准和价格。
 * 舱位等级的选择影响客户的旅行体验和费用。</p>
 * 
 * <p>舱位等级说明：</p>
 * <ul>
 *   <li>ECONOMY：经济舱，基础服务，价格最优惠</li>
 *   <li>PREMIUM_ECONOMY：高级经济舱，介于经济舱和商务舱之间</li>
 *   <li>BUSINESS：商务舱，高端服务，舒适度最高</li>
 * </ul>
 * 
 * <p>服务差异：</p>
 * <ul>
 *   <li>座位舒适度：从经济舱到商务舱逐渐提升</li>
 *   <li>行李额度：商务舱通常有更高的行李限额</li>
 *   <li>餐饮服务：商务舱提供更丰富的餐饮选择</li>
 *   <li>优先服务：商务舱客户享有优先登机等特权</li>
 * </ul>
 * 
 * @author xushu
 * @version 1.0.0
 * @since 2024-01-01
 */
public enum BookingCategoryEnum {

    /**
     * 经济舱
     * <p>基础舱位等级，提供标准服务，价格最优惠</p>
     */
    ECONOMY,

    /**
     * 高级经济舱
     * <p>介于经济舱和商务舱之间的舱位等级，提供更好的舒适度</p>
     */
    PREMIUM_ECONOMY,

    /**
     * 商务舱
     * <p>高端舱位等级，提供最优质的服务和最高的舒适度</p>
     */
    BUSINESS
}
